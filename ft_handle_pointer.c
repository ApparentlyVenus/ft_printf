#include "libft/libft.h"
#include "ft_printf.h"

int	ft_pointer_len(unsigned long n)
{
	int	len;

	len = 1;
	while (n >= 16)
	{
		n /= 16;
		len++;
	}
	return (len);
}

int	ft_handle_pointer(void *ptr)
{
	if (!ptr)
	{
		ft_putstr_fd("(nil)", 1);
		return (5);
	}
	ft_putstr_fd("0x", 1);
	ft_puthex_lower_fd((unsigned long)ptr, 1);
	return (2 + ft_pointer_len((unsigned long)ptr));
}
